# String Type
print(type("string"))

print(type("1"))

print(type("2.2"))

print(type("10/2"))

# Integer Type
print(type(100))

print(type(int(4 / 2)))

print(type(6 // 9))

# Float Type
print(type(1.0))

print(type(8.3))

print(type(2 / 3))

print(type(abs(4 / 7)))

# Complex Type
print(type(complex(2,3)))

print(type(3.14j))

# Boolean Type
a = 10
b = 13
print(a == b)  # False
